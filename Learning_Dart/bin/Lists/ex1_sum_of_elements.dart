/*
Упражнение № 1: Найди сумму элементов в списке.

Учитывая этот список чисел: [1, 2.7, 5, 8.4, 10]

Напишите программу, которая напечатает сумму всех этих значений.
*/

final numbers = [1, 2.7, 5, 8.4, 10];
void main() {
  
  double total = 0;
  
  for (num i in numbers) {
    total += i;
  }
  
  print(total);
}